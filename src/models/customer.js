class Customer {
  constructor(
    id,
    name,
    document,
    birth,
    street,
    number,
    neighborhood,
    complement,
    code,
    city,
    state,
    phone,
    cellphone,
    email,
  ) {
    this.id = id;
    this.name = name;
    this.document = document;
    this.birth = birth;
    this.street = street;
    this.number = number;
    this.neighborhood = neighborhood;
    this.complement = complement;
    this.code = code;
    this.city = city;
    this.state = state;
    this.phone = phone;
    this.cellphone = cellphone;
    this.email = email;
  }
}

export default Customer;
